package asd;

public class VariableTypes {
	int k;
	float p;
	long l;
	char ch;
	String s;
	static String clgname ="UCEK";


	public static void main(String[] args) {		
		VariableTypes obj = new VariableTypes();
		System.out.println("int value k="+obj.k);
		System.out.println("float value k="+obj.p);
		System.out.println("long value k="+obj.l);
		System.out.println("char value k="+obj.ch);
		System.out.println("char value k="+obj.ch);
		System.out.println("String value k="+obj.s);
		System.out.println("clg name static data "+clgname);
		System.out.println("clg name static data "+VariableTypes.clgname);
		

	}

	

}
